def resolve_run_dir(args: argparse.Namespace) -> pathlib.Path:
    if args.run_dir and args.run_id:
        raise SystemExit("use either --run-dir or --run-id, not both")
    if args.run_dir:
        run_dir = args.run_dir.expanduser().resolve()
    elif args.run_id:
        run_dir = (args.archive_root.expanduser().resolve() / "runs" / args.run_id).resolve()
    else:
        raise SystemExit("one of --run-dir or --run-id is required")
    if not run_dir.exists():
        raise SystemExit(f"run directory not found: {run_dir}")
    return run_dir


def resolve_dsn(args: argparse.Namespace) -> str:
    explicit = args.pg_dsn.strip()
    if explicit:
        return explicit
    env_val = os.environ.get(args.dsn_env, "").strip()
    if env_val:
        return env_val
    raise SystemExit(f"provide --pg-dsn or set {args.dsn_env}")


def resolve_run_files(run_dir: pathlib.Path) -> dict[str, pathlib.Path]:
    files = {table: run_dir / filename for table, filename in TABLE_EXPORT_FILES.items()}
    missing = [str(path) for path in files.values() if not path.exists()]
    if missing:
        raise SystemExit("run directory is missing required files:\n" + "\n".join(missing))
    return files


def resolve_work_dir(*, run_dir: pathlib.Path, requested: pathlib.Path | None) -> tuple[pathlib.Path, bool]:
    if requested:
        work_dir = requested.expanduser().resolve()
        work_dir.mkdir(parents=True, exist_ok=True)
        return work_dir, False
    work_dir = pathlib.Path(tempfile.mkdtemp(prefix="session-trajectory-restore-", dir=str(run_dir)))
    return work_dir, True


class ImportLock:
    def __init__(self, path: pathlib.Path) -> None:
        self.path = path
        self._handle: Any | None = None

    def __enter__(self) -> "ImportLock":
        self.path.parent.mkdir(parents=True, exist_ok=True)
        self._handle = self.path.open("a+", encoding="utf-8")
        waiting_logged = False
        while True:
            try:
                fcntl.flock(self._handle.fileno(), fcntl.LOCK_EX | fcntl.LOCK_NB)
                self._handle.seek(0)
                self._handle.truncate()
                self._handle.write(f"pid={os.getpid()} acquired_at={datetime.now(timezone.utc).isoformat()}\n")
                self._handle.flush()
                print(f"[lock] acquired import lock {self.path}", flush=True)
                return self
            except BlockingIOError:
                if not waiting_logged:
                    print(f"[wait] import lock busy {self.path}; waiting for current restore/import to finish", flush=True)
                    waiting_logged = True
                time.sleep(5)

    def __exit__(self, exc_type: Any, exc: Any, tb: Any) -> None:
        if self._handle is None:
            return
        try:
            self._handle.seek(0)
            self._handle.truncate()
            self._handle.flush()
            fcntl.flock(self._handle.fileno(), fcntl.LOCK_UN)
        finally:
            self._handle.close()
            self._handle = None


def main(argv: list[str]) -> int:
    args = parse_args(argv)
    require_binary("psql")
    dsn = resolve_dsn(args)
    run_dir = resolve_run_dir(args)
    run_files = resolve_run_files(run_dir)
    start_time = parse_optional_dt(args.start_time)
    end_time = parse_optional_dt(args.end_time)
    validate_time_window(start_time=start_time, end_time=end_time)

    import_tables = resolve_import_tables(skip_request_exports=args.skip_request_exports)
    ensure_tables_exist(dsn=dsn, schema=args.schema, table_names=import_tables)

    if args.require_storage_prefix.strip():
        verify_storage_prefix(
            dsn=dsn,
            expected_prefix=args.require_storage_prefix.strip(),
            tablespace_name=args.tablespace_name.strip() or None,
        )

    if args.print_storage_workflow:
        print(
            build_storage_workflow_sql(
                schema=args.schema,
                database_name=extract_database_name(dsn),
                tablespace_name=args.storage_tablespace_name.strip(),
                tablespace_location=args.storage_tablespace_location.strip(),
            ),
            flush=True,
        )

    work_dir, should_cleanup = resolve_work_dir(run_dir=run_dir, requested=args.work_dir)
    print(f"[start] run_dir={run_dir} work_dir={work_dir} schema={args.schema}", flush=True)
    try:
        with ImportLock(args.import_lock_path.expanduser().resolve()):
            candidate_file = work_dir / "candidate_sessions.csv"
            candidate_sessions, selected_count = select_candidate_sessions(
                sessions_file=run_files["session_trajectory_sessions"],
                candidate_file=candidate_file,
                start_time=start_time,
                end_time=end_time,
            )
            if selected_count == 0:
                print("[done] selected sessions=0; nothing to import", flush=True)
                return 0

            print(f"[progress] selected sessions={selected_count}", flush=True)
            filtered_counts: dict[str, int] = {}
            filtered_files: dict[str, pathlib.Path] = {}
            for table_name in import_tables:
                filtered_csv = work_dir / f"{table_name}.csv"
                count = filter_table_to_csv(
                    table_name=table_name,
                    input_file=run_files[table_name],
                    output_file=filtered_csv,
                    candidate_sessions=candidate_sessions,
                    progress_every=max(0, args.progress_every),
                )
                filtered_counts[table_name] = count
                filtered_files[table_name] = filtered_csv
                print(f"[progress] prepared {table_name} rows={count}", flush=True)

            if args.truncate_target:
                truncate_target_tables(dsn=dsn, schema=args.schema)
                print("[progress] target tables truncated", flush=True)

            for table_name in import_tables:
                copy_csv_into_table(
                    dsn=dsn,
                    schema=args.schema,
                    table_name=table_name,
                    csv_file=filtered_files[table_name],
                )
                print(f"[progress] imported {table_name} rows={filtered_counts[table_name]}", flush=True)

            print(
                "[done] imported tables="
                + ", ".join(f"{name}:{filtered_counts[name]}" for name in import_tables),
                flush=True,
            )
            print(f"[cursor] work_dir={work_dir}", flush=True)
            return 0
    finally:
        if should_cleanup and not args.keep_work_dir:
            shutil.rmtree(work_dir, ignore_errors=True)


if __name__ == "__main__":
    raise SystemExit(main(os.sys.argv[1:]))
