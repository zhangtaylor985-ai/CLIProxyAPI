#!/usr/bin/env python3
"""Restore archived session trajectory rows from archive files into PostgreSQL.

This tool reads raw JSONL.GZ exports generated by session_trajectory_archive.py,
filters rows by session last_activity_at window, writes table CSV files, and
loads them into existing PostgreSQL tables with psql \\copy.
"""

