def parse_dt(raw: str) -> datetime:
    return datetime.fromisoformat(raw.replace("Z", "+00:00")).astimezone(timezone.utc)


def validate_time_window(*, start_time: datetime | None, end_time: datetime | None) -> None:
    if start_time and end_time and start_time > end_time:
        raise SystemExit("--start-time must be earlier than or equal to --end-time")


def parse_optional_dt(raw: str | None) -> datetime | None:
    value = (raw or "").strip()
    if not value:
        return None
    try:
        return parse_dt(value)
    except ValueError as exc:
        raise SystemExit(f"invalid timestamp {value!r}; expected RFC3339") from exc


def quote_ident(value: str) -> str:
    return '"' + value.replace('"', '""') + '"'


def quote_literal(value: str) -> str:
    return "'" + value.replace("'", "''") + "'"


def require_binary(name: str) -> None:
    if resolve_binary_path(name) is None:
        raise SystemExit(f"required binary not found in PATH: {name}")


def resolve_binary_path(name: str) -> str | None:
    found = shutil.which(name)
    if found:
        return found
    if name == "psql":
        for candidate in PSQL_CANDIDATE_PATHS:
            if pathlib.Path(candidate).exists():
                return candidate
    return None


def resolve_import_tables(*, skip_request_exports: bool) -> list[str]:
    if skip_request_exports:
        return [name for name in IMPORT_ORDER if name != "session_trajectory_request_exports"]
    return list(IMPORT_ORDER)


def read_jsonl_gz(path: pathlib.Path) -> Any:
    with gzip.open(path, "rt", encoding="utf-8", newline="\n") as handle:
        for index, line in enumerate(handle, start=1):
            line = line.strip()
            if not line:
                continue
            try:
                row = json.loads(line)
            except json.JSONDecodeError as exc:
                try:
                    row = json.loads(decode_pg_copy_text_line(line))
                except json.JSONDecodeError as fallback_exc:
                    raise RuntimeError(f"invalid JSON at {path}:{index}") from fallback_exc
            if not isinstance(row, dict):
                raise RuntimeError(f"non-object JSON row at {path}:{index}")
            yield row


def decode_pg_copy_text_line(raw: str) -> str:
    """Reverse PostgreSQL COPY text escaping for a single-column JSON line."""
    out: list[str] = []
    i = 0
    length = len(raw)
    while i < length:
        ch = raw[i]
        if ch != "\\" or i + 1 >= length:
            out.append(ch)
            i += 1
            continue
        nxt = raw[i + 1]
        if nxt == "\\":
            out.append("\\")
            i += 2
            continue
        if nxt == "n":
            out.append("\n")
            i += 2
            continue
        if nxt == "r":
            out.append("\r")
            i += 2
            continue
        if nxt == "t":
            out.append("\t")
            i += 2
            continue
        if nxt == "b":
            out.append("\b")
            i += 2
            continue
        if nxt == "f":
            out.append("\f")
            i += 2
            continue
        if nxt == "v":
            out.append("\v")
            i += 2
            continue
        if i + 3 < length and all("0" <= c <= "7" for c in raw[i + 1 : i + 4]):
            out.append(chr(int(raw[i + 1 : i + 4], 8)))
            i += 4
            continue
        out.append(nxt)
        i += 2
    return "".join(out)


def session_matches_window(
    *,
    last_activity_at: datetime,
    start_time: datetime | None,
    end_time: datetime | None,
) -> bool:
    if start_time and last_activity_at < start_time:
        return False
    if end_time and last_activity_at > end_time:
        return False
    return True


def select_candidate_sessions(
    *,
    sessions_file: pathlib.Path,
    candidate_file: pathlib.Path,
    start_time: datetime | None,
    end_time: datetime | None,
) -> tuple[set[str], int]:
    selected_ids: list[str] = []
    selected_lookup: set[str] = set()
    candidate_file.parent.mkdir(parents=True, exist_ok=True)
    with candidate_file.open("w", encoding="utf-8", newline="\n") as out:
        for row in read_jsonl_gz(sessions_file):
            session_id = str(row.get("id") or "").strip()
            if not session_id:
                continue
            raw_last_activity = str(row.get("last_activity_at") or "").strip()
            if not raw_last_activity:
                continue
            last_activity_at = parse_dt(raw_last_activity)
            if not session_matches_window(
                last_activity_at=last_activity_at,
                start_time=start_time,
                end_time=end_time,
            ):
                continue
            if session_id in selected_lookup:
                continue
            selected_lookup.add(session_id)
            selected_ids.append(session_id)
            out.write(session_id)
            out.write("\n")
    return selected_lookup, len(selected_ids)


def csv_value(value: Any) -> str:
    if value is None:
        return r"\N"
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, (dict, list)):
        return json.dumps(value, separators=(",", ":"))
    return str(value)


def filter_table_to_csv(
    *,
    table_name: str,
    input_file: pathlib.Path,
    output_file: pathlib.Path,
    candidate_sessions: set[str],
    progress_every: int,
) -> int:
    session_key = "id" if table_name == "session_trajectory_sessions" else "session_id"
    columns = TABLE_COLUMNS[table_name]
    count = 0
    scanned = 0
    output_file.parent.mkdir(parents=True, exist_ok=True)
    with output_file.open("w", encoding="utf-8", newline="") as out:
        writer = csv.writer(out)
        for row in read_jsonl_gz(input_file):
            scanned += 1
            session_id = str(row.get(session_key) or "").strip()
            if not session_id or session_id not in candidate_sessions:
                if progress_every > 0 and scanned % progress_every == 0:
                    print(
                        f"[progress] scanning {table_name} scanned_rows={scanned} matched_rows={count}",
                        flush=True,
                    )
                continue
            writer.writerow([csv_value(row.get(col)) for col in columns])
            count += 1
            if progress_every > 0 and scanned % progress_every == 0:
                print(
                    f"[progress] scanning {table_name} scanned_rows={scanned} matched_rows={count}",
                    flush=True,
                )
    return count


def run_command(cmd: list[str], *, input_text: str | None = None) -> str:
    proc = subprocess.run(
        cmd,
        input=input_text,
        text=True,
        capture_output=True,
        check=False,
    )
    if proc.returncode != 0:
        raise RuntimeError(
            "command failed\n"
            f"cmd: {' '.join(cmd)}\n"
            f"stdout:\n{proc.stdout}\n"
            f"stderr:\n{proc.stderr}"
        )
    return proc.stdout


def psql_base_cmd(dsn: str) -> list[str]:
    psql_path = resolve_binary_path("psql")
    if not psql_path:
        raise SystemExit("required binary not found in PATH: psql")
    return [psql_path, dsn, "-q", "-X", "-v", "ON_ERROR_STOP=1", "-P", "pager=off"]


def run_psql_sql(dsn: str, sql: str) -> str:
    return run_command(psql_base_cmd(dsn) + ["-At", "-F", "\t", "-c", sql]).strip()


def run_psql_script(dsn: str, script: str) -> str:
    return run_command(psql_base_cmd(dsn) + ["-f", "-"], input_text=script)


def ensure_tables_exist(*, dsn: str, schema: str, table_names: list[str]) -> None:
    names_sql = ", ".join(quote_literal(name) for name in sorted(table_names))
    sql = (
        "SELECT table_name FROM information_schema.tables "
        f"WHERE table_schema = {quote_literal(schema)} "
        f"AND table_name IN ({names_sql}) ORDER BY table_name"
    )
    found = {line.strip() for line in run_psql_sql(dsn, sql).splitlines() if line.strip()}
    missing = sorted(set(table_names) - found)
    if missing:
        raise SystemExit(
            "target schema is missing required tables: "
            + ", ".join(missing)
            + ". Run scripts/migrate_session_trajectory_pg first."
        )


def truncate_target_tables(*, dsn: str, schema: str) -> None:
    qualified = ", ".join(f"{quote_ident(schema)}.{quote_ident(name)}" for name in TRUNCATE_ORDER)
    run_psql_sql(dsn, f"TRUNCATE {qualified};")


def copy_csv_into_table(
    *,
    dsn: str,
    schema: str,
    table_name: str,
    csv_file: pathlib.Path,
) -> None:
    if csv_file.stat().st_size == 0:
        return
    script = build_copy_csv_script(
        schema=schema,
        table_name=table_name,
        csv_file=csv_file,
    )
    run_psql_script(dsn, script)


def build_copy_csv_script(
    *,
    schema: str,
    table_name: str,
    csv_file: pathlib.Path,
) -> str:
    columns = ", ".join(quote_ident(name) for name in TABLE_COLUMNS[table_name])
    return (
        f"\\copy {quote_ident(schema)}.{quote_ident(table_name)} ({columns}) "
        f"FROM {quote_literal(str(csv_file))} WITH (FORMAT csv, NULL '\\N')\n"
    )


def extract_database_name(dsn: str) -> str:
    parsed = urlparse(dsn)
    path = (parsed.path or "").strip("/")
    return path or "cliproxy"


def build_storage_workflow_sql(
    *,
    schema: str,
    database_name: str,
    tablespace_name: str,
    tablespace_location: str,
) -> str:
    schema_q = quote_ident(schema)
    db_q = quote_ident(database_name)
    ts_q = quote_ident(tablespace_name)
    ts_lit = quote_literal(tablespace_location)
    lines = [
        "-- Run as PostgreSQL superuser and ensure server OS user owns the directory.",
        f"CREATE TABLESPACE {ts_q} LOCATION {ts_lit};",
        f"ALTER DATABASE {db_q} SET default_tablespace = {quote_literal(tablespace_name)};",
    ]
    for table_name in IMPORT_ORDER:
        lines.append(f"ALTER TABLE {schema_q}.{quote_ident(table_name)} SET TABLESPACE {ts_q};")
    return "\n".join(lines)


def verify_storage_prefix(
    *,
    dsn: str,
    expected_prefix: str,
    tablespace_name: str | None,
) -> None:
    prefix = expected_prefix.rstrip("/")
    data_directory = run_psql_sql(dsn, "SHOW data_directory;").strip()
    tablespace_location = ""
    if tablespace_name:
        sql = (
            "SELECT COALESCE(pg_tablespace_location(oid), '') "
            "FROM pg_tablespace "
            f"WHERE spcname = {quote_literal(tablespace_name)}"
        )
        tablespace_location = run_psql_sql(dsn, sql).strip()
    data_ok = data_directory.startswith(prefix)
    tablespace_ok = bool(tablespace_location) and tablespace_location.startswith(prefix)
    if data_ok or tablespace_ok:
        return
    raise SystemExit(
        "storage prefix check failed: neither PostgreSQL data_directory nor selected "
        f"tablespace location is under {expected_prefix!r}. "
        f"data_directory={data_directory!r}, tablespace_location={tablespace_location!r}"
    )


